plt.title('Correlation Matrix of Crime Types')
plt.tight_layout()
plt.show()

"""## Outlier Detection

### Subtask:
Identify and visualize outliers in the numerical crime columns using appropriate statistical methods or plots, such as box plots or Z-scores, to understand extreme values.

**Reasoning**:
Now that the Outlier Detection subtask has been introduced, I will calculate the Z-scores for each numerical crime column to identify outliers. Z-scores help quantify how far away a data point is from the mean in terms of standard deviations.
"""

from scipy.stats import zscore

for column in crime_columns:
    data[f'{column}_zscore'] = np.abs(zscore(data[column]))
    outliers = data[data[f'{column}_zscore'] > 3]

    print(f"\nOutliers in {column} (Z-score > 3):")
    if not outliers.empty:
        print(outliers[['Unit Name', 'Year', column, f'{column}_zscore']].head())
    else:
        print("No outliers found.")

"""**Reasoning**:
Now that Z-scores have been calculated and outliers identified, I will generate box plots for each numerical crime column to visually represent their distributions and further highlight the identified outliers, which directly addresses the visualization aspect of the subtask.


"""

for column in crime_columns:
    plt.figure(figsize=(8, 6))
    sns.boxplot(y=data[column])
    plt.title(f'Box Plot of {column} with Outliers')
    plt.ylabel(column)
    plt.tight_layout()
    plt.show()

