"""## Time-series Analysis of Crime Trends

### Subtask:
Analyze how different crime types and 'Total Cases' have changed over the years by plotting time-series charts for various crime categories and 'Total Cases'.

**Reasoning**:
To perform geographical analysis, I will first group the data by 'Unit Name' and calculate the sum of 'Total Cases' for each unit, as this will help in identifying which units have the highest crime rates.
"""

unit_crime_summary = data.groupby('Unit Name')['Total Cases'].sum().sort_values(ascending=False).reset_index()
print("Top 10 Units by Total Cases:")
print(unit_crime_summary.head(10))

"""**Reasoning**:
Now that the total crime cases per unit have been calculated and sorted, I will create a bar plot to visualize the 'Total Cases' for each 'Unit Name'. This visualization will clearly show the crime distribution across different geographical units, addressing the next step in geographical analysis.


"""

plt.figure(figsize=(12, 8))
sns.barplot(x='Total Cases', y='Unit Name', data=unit_crime_summary)
plt.title('Total Crime Cases by Unit Name')
plt.xlabel('Total Cases')
plt.ylabel('Unit Name')
plt.tight_layout()
plt.show()

"""**Reasoning**:
To begin the geographical analysis, I will group the data by 'Unit Name' and sum all the crime-related columns to get the total count for each crime type per unit, storing it in `unit_crime_types_summary`.


"""

unit_crime_types_summary = data.groupby('Unit Name')[crime_columns].sum().reset_index()
print("Crime type summary by Unit Name:")
print(unit_crime_types_summary.head())

"""**Reasoning**:
Now that `unit_crime_types_summary` has been created, I will iterate through each crime type in `crime_columns` and generate a bar plot. Each plot will show the total count for that crime type per 'Unit Name', sorted in descending order, with appropriate titles and labels. This addresses instructions 2, 3, 4 and 5 of the subtask.


"""

for column in crime_columns:
    plt.figure(figsize=(12, 8))
    # Sort by the current crime column in descending order
    sorted_data = unit_crime_types_summary.sort_values(by=column, ascending=False)
    sns.barplot(x=column, y='Unit Name', data=sorted_data)
    plt.title(f'Total {column} Cases by Unit Name')
    plt.xlabel(f'Total {column} Cases')
    plt.ylabel('Unit Name')
    plt.tight_layout()
    plt.show()

"""## Geographical/Unit-wise Analysis

### Subtask:
Compare crime statistics for specific crime types across different 'Unit Name' categories.

**Reasoning**:
The geographical analysis of crime types by unit has been completed. The next logical step, according to the main task, is to perform correlation analysis between the numerical crime types. I will start by computing the correlation matrix for the `crime_columns`.
"""

correlation_matrix = data[crime_columns].corr()
print("Correlation Matrix of Crime Types:")
print(correlation_matrix)

"""**Reasoning**:
Now that the correlation matrix has been computed, I will visualize it using a heatmap to graphically represent the correlations between different crime types. This will make it easier to identify strong positive or negative relationships, addressing the next logical step in correlation analysis.


"""

